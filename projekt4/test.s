Zad. 7. Napisać program pobierający z linii zleceń trzy argumenty x, y i z (gdzie z jest znakiem „-” lub ”*”)
tak, aby na ekranie terminala po jego uruchomieniu wypisane została suma lub iloczyn liczb x i y
(w zależności od wprowadzonego symbolu operacji z).
Np. w przypadku gdy x = ”12” i y = ”32” a z = ” − ”,
to efektem działania programu powinno być wyprowadzenie na ekran liczby -20.
